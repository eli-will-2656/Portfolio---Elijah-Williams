# This program will explore all the different algebraic operations that can be performed using Python
x = 5
y = 3

print("x =", x)
print("y = ", y)

print("x + y =", x+y)
print("x - y =", x-y)
print(x*y)
print(x/y)

# Here we have to special operators
print(x % y)
print(x, "floored", y, "is", x // y, "and is of type", type(x//y), sep="  ") 
print(x**y)

